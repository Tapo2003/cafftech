        <script>
            $("#contactname").focus();
            function contactUs(){

           
            var name        = $("#contactname").val();
            var email       = $("#contactemail").val();
            var phoneNumber = $("#contactnumber").val();
            var subject     = $("#contactsubject").val();
            var message     = $("#contactmessage").val();

           

            if(name == '' || email == '' || phoneNumber == '' || subject == '' || message == ''){
                alert("Please fill up the form");
            }else{
                var response = confirm("You are sending us a message");
                if(response = true){
                    var formdata = $("form#contact-form").serialize();
                    $.post("app/processContact.php",formdata).done(function(returndata){
                        if(returndata == 1){
                            alert("You have been successfully sent us a message");
                            clearFields();
                        }else if(returndata == 0){
                            alert("Unable to send us a message");
                                    
                        }else{
                            alert("A problem occured");
                        }
                    });
                }
            }
        } 
        function clearFields(){
            $("#contactname").val('');
            $("#contactemail").val('');
            $("#contactnumber").val('');
            $("#contactsubject").val(''); 
            $("#contactmessage").val('');            
        }      
            
